package com.mycompany.ass2.chapter4;

public class Program_4_10 {
	public static void main(String[]args)
	{
		// single selection if statement
		int x = 1;
		if (x>1)
			System.out.println("X is larger than 1");
		
		// while repetition statement
		int i=0;
		while(i < 3)
		{
			i++;
		}
		
		// depending on the conditions, both statements can run once, however the while loop is capable of
		// running multiple iterations
		
	}
}
