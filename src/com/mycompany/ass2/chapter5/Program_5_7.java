package com.mycompany.ass2.chapter5;

public class Program_5_7 {

	public static void main(String[]args)
	{
		int counter =1;
		
		do
		{
			System.out.printf("%d  ",  counter);
			++counter;
		}while(counter<=10);		// end do...while
	}
}	// end class
